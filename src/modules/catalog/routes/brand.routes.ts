// ==========================================================
// Imports
// ==========================================================

import { Router } from 'express';

import { brandController } from '../controllers/BrandController';

// ==========================================================
// Router
// ==========================================================

const router = Router();

// ==========================================================
// Routes
// ==========================================================

router.get('/', async (req, res, next) => {
  try {
    await brandController.getBrands(req, res);
  } catch (error) {
    next(error);
  }
});

// ==========================================================
// Exports
// ==========================================================

export default router;